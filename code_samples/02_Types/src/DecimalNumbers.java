public class DecimalNumbers {
    public static void main (String[] args) {
        // PRIMITIVE DATA TYPES

        // DECIMAL NUMBERS

        // float
        // single precision (7 digits after dot)
        // 32 bit
        float myFloat = 5f;

        // double (default)
        // double precision (16 digits after dot)
        // 64 bit
        // (faster on many modern computers)
        double myDouble = 5d;
    }
}
